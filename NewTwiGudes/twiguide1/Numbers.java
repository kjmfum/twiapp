package com.learnakantwi.twiguides;

public class Numbers {

    String figure;
    String numberWord;

    public Numbers(String figure, String numberWord) {
        this.figure = figure;
        this.numberWord = numberWord;
    }

    public Numbers() {
        this.figure = figure;
        this.numberWord = numberWord;
    }

    public String getFigure() {
        return figure;
    }

    public void setFigure(String figure) {
        this.figure = figure;
    }

    public String getNumberWord() {
        return numberWord;
    }

    public void setNumberWord(String numberWord) {
        this.numberWord = numberWord;
    }
}

