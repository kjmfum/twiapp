package com.learnakantwi.twiguides;

public class Months {

    String englishMonths;
    String twiMonths;

    public Months(String englishMonths, String twiMonths) {
        this.englishMonths = englishMonths;
        this.twiMonths = twiMonths;
    }

    public String getEnglishMonths() {
        return englishMonths;
    }

    public void setEnglishMonths(String englishMonths) {
        this.englishMonths = englishMonths;
    }

    public String getTwiMonths() {
        return twiMonths;
    }

    public void setTwiMonths(String twiMonths) {
        this.twiMonths = twiMonths;
    }
}
