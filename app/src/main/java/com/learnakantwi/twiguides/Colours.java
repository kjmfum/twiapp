package com.learnakantwi.twiguides;

public class Colours {

    String englishColours;
    String twiColours;

    public Colours(String englishColours, String twiColours) {
        this.englishColours = englishColours;
        this.twiColours = twiColours;
    }

    public String getEnglishColours() {
        return englishColours;
    }

    public void setEnglishColours(String englishColours) {
        this.englishColours = englishColours;
    }

    public String getTwiColours() {
        return twiColours;
    }

    public void setTwiColours(String twiColours) {
        this.twiColours = twiColours;
    }
}
