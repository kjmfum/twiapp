package com.learnakantwi.twiguides;

public class Family {

    String englishFamily;
    String twiFamily;

    public Family(String englishFamily, String twiFamily) {
        this.englishFamily = englishFamily;
        this.twiFamily = twiFamily;
    }

    public String getEnglishFamily() {
        return englishFamily;
    }

    public void setEnglishFamily(String englishFamily) {
        this.englishFamily = englishFamily;
    }

    public String getTwiFamily() {
        return twiFamily;
    }

    public void setTwiFamily(String twiFamily) {
        this.twiFamily = twiFamily;
    }
}
